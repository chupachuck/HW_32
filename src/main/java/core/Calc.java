package core;

public class Calc {

	public static void main(String[] args) {
		
		
	}
	
	public static int add2(int a, int b) {
		int c = a + b;
		return c;
	}
	
	public static double add3(double a, double b, double c) {
		double d = a + b + c;
		return d;
	}
	
	public static double add4(double a, double b, double c, double d) {
		double e = a + b + c + d;
		return e;
	}
	
	public static double subs2(double a, double b) {
		double c = a - b;
		return c;
	}
	
	public static double subs3(double a, double b, double c) {
		double d = a - b - c;
		return d;
	}
	
	public static double subs4(double a, double b, double c, double d) {
		double e = a - b - c - d;
		return e;
	}
	
	public static double mult2(double a, double b) {
		double c = a * b;
		return c;
	}
	
	public static double mult3(double a, double b, double c) {
		double d = a * b * c;
		return d;
	}
	
	public static double mult4(double a, double b, double c, double d) {
		double e = a * b * c * d;
		return e;
	}
	
	public static double div2(double a, double b) {
		double c = a / b;
		return c;
	}
	
	public static double div3(double a, double b, double c) {
		double d = a / b / c;
		return d;
	}
	
	public static double div4(double a, double b, double c, double d) {
		double e = a / b / c / d;
		return e;
	}
	
	


}
